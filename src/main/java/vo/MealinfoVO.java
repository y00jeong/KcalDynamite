package vo;

public class MealinfoVO {
	private int mfnum;
	private int mealnum;
	private String menu;
	private int intake;
	private int kcal;
	
	public MealinfoVO() {
		
	}

	public int getMfnum() {
		return mfnum;
	}

	public void setMfnum(int mfnum) {
		this.mfnum = mfnum;
	}

	public int getMealnum() {
		return mealnum;
	}

	public void setMealnum(int mealnum) {
		this.mealnum = mealnum;
	}

	public String getMenu() {
		return menu;
	}

	public void setMenu(String menu) {
		this.menu = menu;
	}

	public int getIntake() {
		return intake;
	}

	public void setIntake(int intake) {
		this.intake = intake;
	}

	public int getKcal() {
		return kcal;
	}

	public void setKcal(int kcal) {
		this.kcal = kcal;
	}
	
	
}
